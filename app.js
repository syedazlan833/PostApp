function aquamarine() {
    var inputmain = document.getElementById("maininput");
    inputmain.style.backgroundColor = "aquamarine"
    inputmain.style.color = "black"
    inputmain.style.boxShadow="0px 2px 6px 2px black"
}
function aqua() {
    var inputmain = document.getElementById("maininput");
    inputmain.style.backgroundColor = "aqua"
    inputmain.style.color = "black"
}